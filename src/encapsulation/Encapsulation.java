package encapsulation;

public class Encapsulation {
private int id;
private String name;
private float salary;

public void setId(int Id) {
	this.id=Id;
}
public void setName(String Name) {
	this.name=Name;
}
public void setSalary(float Salary) {
	this.salary=Salary;
}
public int getId()
{
	return id;
}
public String getName()
{
	return name;
}
public float getSalary()
{
	return salary;
}
}



